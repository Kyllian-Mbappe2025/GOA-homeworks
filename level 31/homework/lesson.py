# 1) რა არის ფუნქცია და რაში გვეხმარება?
# ფუნქცია არის კოდის ბლოკი, რომელსაც ვარქმევთ სახელს და ვიძახებთ როცა გვჭირდება.
# გვეხმარება კოდის გამარტივებაში,  ლოგიკის დალაგებაში და გამეორების თავიდან აცილებაში.

def nums(n1, n2):
    return (n1 + n2) * 2

def avragecount(n1, n2):
    return (n1 + n2) / 2

def func1(n1, n2, n3):
    return (n1 * n2) / n3

print("nums(3, 5):", nums(3, 5))
print("avragecount(10, 20):", avragecount(10, 20))
print("func1(6, 4, 2):", func1(6, 4, 2))